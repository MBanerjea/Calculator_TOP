// Basic functions

// Operate
function operate(operator, a, b) {
  switch (operator) {
    case '+':
      return add(a, b);

    case '-':
      return subtract(a, b);

    case '*':
      return multiply(a, b);

    case '/':
      return divide(a, b);

    default:
      break;
  }
}

// Add
function add(numbers) {
  return a + b;
}

// Subtract
function subtract(numbers) {
  return a - b;
}

// Multiply
function multiply(numbers) {
  return a * b;
}

// Divide
function divide(a, b) {
  return a / b;
}

